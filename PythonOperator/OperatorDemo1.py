#python的运算符实例
#'+'加号
# 俩个对象相加(可以是俩个数字，也可以是俩个字符串(将俩个字符串连接))
a=7+8
print(a)
b="GOOD"+"Job"
print(b)

#'-'减号
#取一个数字的相反数或者实现俩个数字相减
c=-7
print(c)
print(19-1)

#'*'乘号
#如果是数字则进行乘法运算，字符串则复制若干次
d=4*7
print(d)
e="hello"*7
print(e)

#'/'除号
#表示俩个数字相除(Python 3.0中会直接输出正确的值)
f=7/2
print(f)

#'**'求幂运算
g=2**3
print(g)

#'<'小于号 返回一个布尔值
h=3<7
print(h)
