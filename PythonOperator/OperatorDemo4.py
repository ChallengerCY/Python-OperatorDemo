#Python的优先级
a=2+7*8
print(a)

b=9>7
print(b)

c=4*2**3
print(c)

d=2+4*-2
print(d)

e=2+4*2/4
print(e)

f=3<<2+1
print(f)

g=2*3+5<=5+1*2
print(g)

#总结:函数调用，寻址，下标 > 幂运算 > 反转运算 > 正负号 > * / % > + - > << >> > & | ^ > 比较运算符 > and or not > lambda

#优先级使用规律

#1、一般情况下是左结合的
print(4+6+5*6+6)
#2、出现赋值运算一般是右结合
a=8+91
print(a)
#3、可以使用加括号的方式提高优先级
print((2+5)*6)
print(((2+5)+5)*6)